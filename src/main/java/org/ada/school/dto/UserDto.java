package org.ada.school.dto;

/**
 * The type User dto.
 */
public class UserDto
{
    /**
     * The Name.
     */
    String name;

    /**
     * The Email.
     */
    String email;

    /**
     * The Last name.
     */
    String lastName;

    /**
     * Instantiates a new User dto.
     */
    public UserDto()
    {
    }

    /**
     * Gets name.
     *
     * @return the name
     */
    public String getName()
    {
        return name;
    }

    /**
     * Gets email.
     *
     * @return the email
     */
    public String getEmail()
    {
        return email;
    }

    /**
     * Gets last name.
     *
     * @return the last name
     */
    public String getLastName()
    {
        return lastName;
    }
}
